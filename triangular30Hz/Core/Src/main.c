void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
    static uint8_t channel = 1;
    static uint8_t first_run = 1;
    static uint16_t ccr1 = 0;
    static uint16_t ccr2 = 0;
    static int8_t direction1 = 1;
    static int8_t direction2 = 1;
    static uint8_t cycle_complete = 0;

    const uint16_t step_size = 60;  // Define step size as constant
    const uint16_t max_value = 4999;



    if (htim->Instance == TIM1)
    {
        // Handle the first run - start with Channel 1
        if (first_run)
        {
            // Make sure only Channel 1 is active initially
            __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_2, 0);


            // Ensure Channel 1 is running
            HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
            HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_1);


            channel = 1;
            ccr1 = 0;
            direction1 = 1;  // Start going up
            cycle_complete = 0;
            first_run = 0;
        }

        if (channel == 1)
        {
            // Update Channel 1
            if (direction1 == 1)
            {
                ccr1 += step_size;
                if (ccr1 >= max_value)
                {
                    ccr1 = max_value;
                    direction1 = -1;
                }
            }
            else // Going down (direction1 == -1)
            {
                if (ccr1 >= step_size)
                {
                    ccr1 -= step_size;
                }
                else
                {
                    ccr1 = 0;
                    cycle_complete = 1;  // Full cycle completed (0→4999→0)
                }
            }

            // Update Channel 1 compare value
            __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, ccr1);

            // Switch to Channel 2 only after complete triangular cycle
            if (cycle_complete)
            {
                // Stop Channel 1
                __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, 0);


                // Initialize and start Channel 2
                ccr2 = 0;
                direction2 = 1;  // Start going up
                __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_2, 0);
                HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_2);
                HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_2);


                channel = 2;
                cycle_complete = 0;
                direction1 = 1;  // Reset direction for next time
            }
        }
        else // channel == 2
        {
            // Update Channel 2
            if (direction2 == 1) // Going up
            {
                ccr2 += step_size;
                if (ccr2 >= max_value)
                {
                    ccr2 = max_value;  // Clamp to maximum
                    direction2 = -1;   // Start going down
                }
            }
            else // Going down (direction2 == -1)
            {
                if (ccr2 >= step_size)
                {
                    ccr2 -= step_size;
                }
                else
                {
                    ccr2 = 0;           // Clamp to minimum
                    cycle_complete = 1;  // Full cycle completed (0→4999→0)
                }
            }

            // Update Channel 2 compare value
            __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_2, ccr2);

            // Switch to Channel 1 only after complete triangular cycle
            if (cycle_complete)
            {
                // Stop Channel 2
                __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_2, 0);


                // Initialize and start Channel 1
                ccr1 = 0;
                direction1 = 1;  // Start going up
                __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, 0);
                HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
                HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_1);


                channel = 1;
                cycle_complete = 0;
                direction2 = 1;  // Reset direction for next time
            }
        }
    }
}





